package it.dantar.games.pingpong.models;

public class FantascattiPiece {

	String shape;
	String color;
	
	public String getShape() {
		return shape;
	}
	public FantascattiPiece setShape(String shape) {
		this.shape = shape;
		return this;
	}
	public String getColor() {
		return color;
	}
	public FantascattiPiece setColor(String color) {
		this.color = color;
		return this;
	}
	
}
